"use client";

/*
  This page is fully client-side because it has an interactive form
  and computes results using JavaScript without needing a server.
*/

import { useState } from "react";
import { Lightbulb, Shield, TrendingUp, ArrowRight } from "lucide-react";

/*
  Platform stats derived from portfolio_insights.json generated by the Python pipeline.
  These numbers represent aggregated, k-anonymous metrics across all 3 clients
  running campaigns throughout 2024. Each number is traceable to a specific field
  in the pipeline output.

  avg_ctr_pct: from avg_ctr_by_platform
  cpm_median:  from cpm_ranges_by_platform
  avg_roas:    averaged from monthly_platform_roas across all 12 months of 2024
*/
const PLATFORM_DATA: Record<string, {
  avg_ctr_pct: number;
  cpm_median: number;
  avg_roas: number;
  cpm_min: number;
}> = {
  "Google Ads": {
    avg_ctr_pct: 3.973,
    cpm_median:  75.91,
    avg_roas:    4.14,
    cpm_min:     15.09,
  },
  "Meta Ads": {
    avg_ctr_pct: 2.483,
    cpm_median:  28.73,
    avg_roas:    7.14,
    cpm_min:     4.68,
  },
  "TikTok Ads": {
    avg_ctr_pct: 5.516,
    cpm_median:  49.48,
    avg_roas:    9.69,
    cpm_min:     9.11,
  },
};

/* The three ad platforms available in the portfolio */
const PLATFORMS = ["Google Ads", "Meta Ads", "TikTok Ads"];

/*
  A single recommendation entry: which platform, what percentage, how much money,
  what rank it received, and the explanation for why it was chosen.
*/
type PlatformRec = {
  platform: string;
  allocation_pct: number;
  budget_usd: number;
  rank: "Primary" | "Secondary" | "Supporting";
  reason: string;
  score: number;
  score_label: string;
};

/*
  Scores each platform based on the advertiser's goal, then returns
  the score and a plain-English label explaining what was measured.

  Brand Awareness: we want the most clicks per dollar of impression cost.
    Score = CTR divided by CPM (higher CTR per dollar = better reach value)

  More Conversions: we want the best return for every dollar spent.
    Score = average ROAS (how many dollars come back for each dollar in)

  Website Traffic: we want the most clicks, regardless of cost.
    Score = raw CTR percentage
*/
function scoreForGoal(platform: string, goal: string): { score: number; label: string } {
  const stats = PLATFORM_DATA[platform];
  if (goal === "awareness") {
    const score = stats.avg_ctr_pct / stats.cpm_median;
    return {
      score,
      label: `CTR/CPM ratio: ${score.toFixed(4)} (${stats.avg_ctr_pct.toFixed(2)}% CTR at $${stats.cpm_median.toFixed(2)} CPM)`,
    };
  }
  if (goal === "conversions") {
    return {
      score: stats.avg_roas,
      label: `Avg ROAS: ${stats.avg_roas.toFixed(2)}x (each $1 spent returns $${stats.avg_roas.toFixed(2)})`,
    };
  }
  /* Default: traffic - scored by raw CTR */
  return {
    score: stats.avg_ctr_pct,
    label: `Avg CTR: ${stats.avg_ctr_pct.toFixed(2)}% (this share of people who saw the ad clicked it)`,
  };
}

/*
  Adjusts a platform's score based on the target audience age.
  TikTok skews younger, so it gets a boost for under-25 audiences
  and a penalty for over-40 audiences where adoption is lower.
  Google gets a slight boost for older audiences who use search more.
*/
function applyAgeAdjustment(platform: string, score: number, age: string): number {
  if (age === "under25") {
    if (platform === "TikTok Ads")  return score * 1.20;
    if (platform === "Google Ads")  return score * 0.90;
  }
  if (age === "over40") {
    if (platform === "TikTok Ads")  return score * 0.70;
    if (platform === "Google Ads")  return score * 1.10;
    if (platform === "Meta Ads")    return score * 1.05;
  }
  return score;
}

/*
  Turns platform scores into allocation percentages based on the budget size.
  Smaller budgets concentrate more on fewer platforms so they don't spread
  too thin and become ineffective on any single channel.
*/
function buildAllocations(
  ranked: string[],
  budget: number
): Array<{ platform: string; pct: number; rank: PlatformRec["rank"] }> {
  if (budget < 5000) {
    /* Very small budget: only two platforms, majority goes to the top one */
    return [
      { platform: ranked[0], pct: 70, rank: "Primary"    },
      { platform: ranked[1], pct: 30, rank: "Secondary"  },
    ];
  }
  if (budget < 10000) {
    /* Mid budget: three platforms but still concentrated */
    return [
      { platform: ranked[0], pct: 60, rank: "Primary"    },
      { platform: ranked[1], pct: 30, rank: "Secondary"  },
      { platform: ranked[2], pct: 10, rank: "Supporting" },
    ];
  }
  /* Larger budget: more balanced across all three platforms */
  return [
    { platform: ranked[0], pct: 50, rank: "Primary"    },
    { platform: ranked[1], pct: 30, rank: "Secondary"  },
    { platform: ranked[2], pct: 20, rank: "Supporting" },
  ];
}

/*
  Generates a plain-English reason explaining why a platform received
  its particular allocation, grounded in the specific metric that drove it.
*/
function buildReason(platform: string, rank: PlatformRec["rank"], goal: string): string {
  const stats = PLATFORM_DATA[platform];
  if (rank === "Primary") {
    if (goal === "awareness")    return `Highest value for reach: ${stats.avg_ctr_pct.toFixed(2)}% CTR at a median CPM of $${stats.cpm_median.toFixed(2)} means you get the most clicks per dollar of impression spend.`;
    if (goal === "conversions")  return `Strongest return on spend: ${stats.avg_roas.toFixed(2)}x average ROAS means each dollar invested brings back $${stats.avg_roas.toFixed(2)} in revenue on average.`;
    return `Highest click rate: ${stats.avg_ctr_pct.toFixed(2)}% of people who see the ad click it, the highest of all three platforms.`;
  }
  if (rank === "Secondary") {
    if (goal === "awareness")    return `Good reach value as a secondary channel. CPM of $${stats.cpm_median.toFixed(2)} is within range and provides audience diversity.`;
    if (goal === "conversions")  return `Second-best ROAS at ${stats.avg_roas.toFixed(2)}x. Worth allocating budget here to diversify and reduce dependence on one platform.`;
    return `A reliable secondary traffic driver at ${stats.avg_ctr_pct.toFixed(2)}% CTR. Adds volume without too much extra cost.`;
  }
  /* Supporting */
  return `Use a small portion here to test performance and maintain presence. Data from 3 clients shows a CPM range of $${stats.cpm_min.toFixed(2)} to $${stats.cpm_median.toFixed(2)}.`;
}

/*
  The main recommendation function. Takes the form inputs and returns
  a list of platform recommendations sorted by allocation size.
*/
function computeRecommendation(
  goal: string,
  budget: number,
  audienceAge: string
): PlatformRec[] {
  /* Score each platform for the given goal */
  const scored = PLATFORMS.map((platform) => {
    const { score, label } = scoreForGoal(platform, goal);
    const adjusted = applyAgeAdjustment(platform, score, audienceAge);
    return { platform, score: adjusted, label };
  });

  /* Sort platforms by score, highest first */
  scored.sort((a, b) => b.score - a.score);
  const ranked = scored.map((s) => s.platform);

  /* Build allocation percentages */
  const allocs = buildAllocations(ranked, budget);

  /* Return the final list with all details filled in */
  return allocs.map((alloc) => {
    const scoreEntry = scored.find((s) => s.platform === alloc.platform)!;
    return {
      platform:       alloc.platform,
      allocation_pct: alloc.pct,
      budget_usd:     Math.round((alloc.pct / 100) * budget),
      rank:           alloc.rank,
      reason:         buildReason(alloc.platform, alloc.rank, goal),
      score:          scoreEntry.score,
      score_label:    scoreEntry.label,
    };
  });
}

/* Color dot for each platform, matching all other charts in the app */
const PLATFORM_COLORS: Record<string, string> = {
  "Meta Ads":   "#FF5656",
  "Google Ads": "#FF8A56",
  "TikTok Ads": "#F472B6",
};

/* Background color for each rank badge */
const RANK_STYLES: Record<PlatformRec["rank"], string> = {
  "Primary":    "bg-primary/10 text-primary border border-primary/20",
  "Secondary":  "bg-blue-50 text-blue-700 border border-blue-200",
  "Supporting": "bg-gray-100 text-gray-600 border border-gray-200",
};

